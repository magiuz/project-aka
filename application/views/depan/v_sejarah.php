                    <h4>II. Perkembangan Sekolah</h4>

                    <p>SMA Al-Kautsar pertama kali menerima murid pada tahun 1993/1994. Sebagai sekolah yang baru berdiri dan belum dikenal oleh masyarakat menyebabkan adanya kekhawatiran dari pengurus Yayasan tentang ada tidaknya minat masyarakat menyekolahkan anaknya di SMA Al-kautsar, sehingga pada tahun itu kebijakannya adalah 10  calon murid pendaftar pertama langsung diterima. Berkat penyebaran informasi   secara langsung ke sekolah-sekolah seperti Kalianda, Metro/lampung Tengah, Kotabumi dan Pringsewu maka pada tahun pertama mendapatkan siswa sebanyak kurang lebih 210 orang, yang pada waktu itu masih menyatu dengan SMP Al-Kautsar baik gedung maupun guru dan karyawannya. Yang unik pada tahun itu adalah hingga penataran  P4 selesai sekolah belum memiliki Kepala Sekolah.
                        Tahun 1994-1996 sekolah sering mendapat  kunjungan para menteri seperti Menteri Pendidikan dan Kebudayaan RI, Menteri Riset dan Teknologi RI, Menteri Agama RI dan pejabat-pejabat pusat lainnya. Prestasi demi prestasi diraih oleh SMA Al-Kautsar baik pada bidang akademis maupun non akademis di Bandar Lampung maupun Lampung bahkan pada event nasional.
                        Memasuki  tahun 2002/2003 SMA Al-Kautsar memasuki babak baru dengan dibukanya kelas unggul akademis. Dari kelas unggul inilah akhirnya perkembangan sekolah mengalami kemajuan yang cukup pesat, bahkan bisa dikatakan sejajar dengan sekolah-sekolah vafourit di Lampung seperti SMAN 2 dan SMA Xaverius pada beberapa bidang. Dilihat  dari prosentase kelulusan dan siswa yang diterima di PTN atau PTS vafouritpun dari tahun ke tahun mengalami perkembangan yang sangat tajam. Sukses dalam pengelolaan kelas unggul, maka pada tahun pelajaran 2004/2005 SMA Al-Kautsar membuka kelas Plus. Target dari kelas ini adalah mampu menyamai prestasi dari kelas unggul dengan dukungan sarana dan prasarana, yang saat ini bernama kelas Bilingual yang sedang berupaya menuju tingkat kelas Internasional dengan manajement dan kurikulum serta pengelolaannya mengacu pada program sekolah internasional, oleh karena itu perlu adanya dukungan dari semua fihak khususnya Yayasan Al-Kautsar terkait dengan pendanaannya.
                    </p>
                    
                    <h4>III. VISI, MISI DAN FAKTOR PENENTU KEBERHASILAN</h4>
                    
                    <p><b>3.1. Visi</b></p>
                    <p>Dalam mewujudkan visi perguruan yaitu ”Al-Kautsar perguruan unggul islami berwawasan global” maka disusunlah Visi SMA Al-Kautsar yaitu Mewujudkan sekolah yang Unggul, Islami     dan berwawasan Global</p>
                    
                    <p><b>3.2. Misi</b></p>
                     <li><b>Untuk mencapai visi tersebut maka misi sekolah adalah :</b></li>
                    <p>a. Meningkatkan pengetahuan , pemahaman dan pengamalan  keislaman kepada seluruh warga sekolah dalam kehidupan sehari-hari </p>
                    <p>b. Meningkatkan kompetensi guru dan karyawan dengan pengetahuan dan ketrampilan yang dibutuhkan dalam pelaksanaan tugas dan kewajiban</p>
                    <p>c. Mewujudkan sekolah sebagai tempat yang kondusif bagi  berlangsungnya kegiatan belajar mengajar </p>
                    <p>d. Melengkapi sarana dan prasarana yang dibutuhkan   untuk  tumbuh dan berkembangnya bakat dan potensi anak didik</p>
                    
                    <p><b>3.3. Nilai</b></p>
                     <li><b>Nilai (value) adalah norma-norma atau prinsip-prinsip agung yang menjadi acuan semua elemen dalam lembaga atau organisasi untuk membangun sikap dalam mencapai visi atau misi yang telah dirumuskan. Nilai-nilai yang terus dipertahankan dan ditingkatkan khususnya di lingkungan SMA Al-Kautsar adalah :</b></li>
                    
                    <p>a. Credibility yaitu  selalu jujur kepada diri sendiri, orang lain dan kepada Allah Swt </p>
                    <p>b. Togetherness yaitu  semangat kebersamaan dalam setiap situasi dan kondisi</p>
                    <p>c. Emphaty yaitu ikut merasakan masalah yang dihadapi orang lain </p>
                    <p>d. Assit yaitu kesediaan untuk ikhlas membantu orang lain</p>
                    <p>e. Maturity yaitu kematangan dalam dalam menghadapi permasalahan</p>
                    <p>f. Respect yaitu saling hormat menghormati antar sesama</p>
                    <p>g. Kindness yaitu prilaku sopan, santun, rendah hati dan menciptakan suasana kesejukan</p>
                    <p>h. Integrity yaitu tidak mudah terpengaruh untk melaksanakan prilaku menyimpang</p>
                    <p>i. Inovative yaitu selalu berupaya menciptakan sesuatu yang baru dan bermanfaat bagi lembaga dan organisasi</p>
                    <p>j. Advantage yaitu memiliki keyakinan untuk menjadi yang terbaik</p>
                    <p>k. Flexibility yaitu tidak kaku dalam menyikapi suatu permasalahan</p>
                    <p>l. Wisdom yaitu memiliki kearifan  dalam bertindak dan berprilaku</p>
                    
                   
                  


