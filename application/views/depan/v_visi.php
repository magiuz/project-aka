                    <p>Tujuan pendidikan menengah adalah meningkatkan kecerdasan, pengetahuan, kepribadian, akhlak mulia, serta keterampilan untuk hidup mandiri dan mengikuti pendidikan lebih lanjut.</p>

                    <li><b>3. Tujuan SMA S Al Kautsar adalah sebagai berikut:</b></li>
