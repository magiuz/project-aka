                    <li><b>1. Tujuan Pendidikan Nasional</b></li>

                    <p>Tujuan pendidikan nasional tercantum dalam Undang-Undang Nomor 20 Tahun 2003 tentang Sistem Pendidikan Nasional, pasal 3, “ mengembangkan potensi peserta didik agar menjadi manusia yang beriman dan bertagwa kepada Tuhan Yang Maha Esa, berakhlak mulia, sehat, berilmu, cakap, kreatif, mandiri, dan menjadi warga negara yang demokratis serta bertanggung jawab”.</p>

